          <p>Social media platforms often establish community guidelines to govern user behavior and content. These guidelines outline acceptable and unacceptable conduct, including rules related to hate speech, harassment, and content moderation. Familiarizing yourself with these guidelines can help maintain a positive and safe online environment.</p>
          <button class="read-more-btn">Read More</button>
          <div class="more-info" style="display: none;">
            <p>Community guidelines are rules of conduct that outline what is and is not allowed on a social media platform. These guidelines typically cover topics such as hate speech, harassment, nudity, violence, and other forms of inappropriate content. They help create a safe and welcoming environment for users and ensure that the platform remains a place for constructive dialogue and interaction.</p>
