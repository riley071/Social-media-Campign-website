          <h2>Children's Online Privacy Protection Act (COPPA)</h2>
          <p>COPPA is a U.S. federal law designed to protect the privacy of children under 13 years old online. It imposes requirements on website operators and online service providers, including social media platforms, regarding the collection and use of personal information from children.</p>
          <button class="read-more-btn">Read More</button>
          <div class="more-info" style="display: none;">
            <p>The Children's Online Privacy Protection Act (COPPA) is a U.S. federal law designed to protect the privacy of children under 13 years old. It places parents in control over what information is collected from their children online, requires parental consent for the collection of any personal information, and establishes certain requirements for websites and online services directed at children.</p>
          </div>
        </div>
